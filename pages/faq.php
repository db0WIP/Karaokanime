<div class="quest" id="faq01">Karaokanime, c'est quoi ?</div>
Un site proposant des karaokés d'animes (ou autre) en streaming et/ou téléchargement, ainsi que les paroles des chansons.

<div class="quest" id="faq02">Qui s'occupe de Karaokanime ?</div>
Ryocu et db0, les mêmes deux étudiants à l'origine de <a href="http://anime-ultime.net" target="_blank">Anime-Ultime</a>, <a href="http://stream-anime.org" target="_blank">Stream-Anime</a>, <a href="http://zerofansub.net" target="_blank">Zéro fansub</a> ainsi que tout les site de la <a href="http://db0.fr" target="_blank">db0 company</a>.

<div class="quest" id="faq03">Qui fait les karaokés sur Karaokanime ?</div>
N'importe qui peut proposer son propore karaoké. La plupart du temps, ce sont des particuliers qui font ça pour le plaisir ou pour une équipe de sous-titrage bénévole (fansub).

<div class="quest" id="faq04">Et moi, je peux faire un karaoké ?</div>
Oui. Mais cela demande une certaine expérience dans le milieu. Des <a href="?karaoke=tutoriaux">tutoriaux</a> sont proposés sur le site pour réaliser un karaoké.

<div class="quest" id="faq05">Le karaoké que j'ai fait a été supprimé. Pourquoi ?</div>
1 . Il se peut que la vidéo ait eu un problème au moment de l'encodage, ou que le format ne soit pas supporté. Réessayer de l'envoyer ou <a href="?karaoke=contact">contacter un administrateur</a>.<br />
2 . Le karaoké a été envoyé sur le site sans l'autorisation de la personne l'ayant réalisé, et celle-ci s'est plaint et a demandé sa suppression du site.

<div class="quest" id="faq06">J'ai trouvé un karaoké sympa, mais je n'ai pas l'autorisation du karamaker. Je l'envoie quand même ?</div>
Si ce karaoké se trouve sur un anime fansubbé, il suffit de contacter l'équipe de fansub. Sinon, n'envoyez pas ce karaoké ou essayez de le refaire.

<div class="quest" id="faq07">Mon karaoké se trouve sur le site, mais je ne suis pas d'accord.</div>
La page <a href="?karaoke=contact">Contact</a> permet de demander à ce que le karaoké soit supprimé.

<div class="quest" id="faq08">Pourquoi les vidéos sont lentes à se charger par moment ?</div>
Aux "heures de pointes" (généralement le soir), le nombre d'utilisateurs en ligne est plus élevé que la normale, et cela peut provoquer un ralentissement au niveau de la lecture des vidéos. Il se peut aussi que cela vienne de votre connexion.

<div class="quest" id="faq09">Pourquoi ne peut-on pas télécharger certaines vidéos ?</div>
C'est à la personne qui uploade la vidéo du karaoké de décider si elle autorise ou non le téléchargement.

<div class="quest" id="faq10">Qu'est-ce que signifie "OP" ou "ED" ?</div>
"OP" signigie "Opening", c'est-à-dire le générique de début d'une série.<br />À l'inverse, "ED" signifie "Ending", le générique de fin.

<div class="quest" id="faq11">C'est quoi l'"id" ?</div>
C'est ce qui permet de repérer le numéro du karaoké sur le site.

<div class="quest" id="faq12">Pourquoi je vois des petits carrés à la place des paroles originales ?</div>
Cela signifie que les caractères japonais ne sont pas activés sur ton ordinateur. Il faut chercher dans la configuration de l'ordinateur et souvent, l'installation de la langue nécessite le CD d'installation du système.

<div class="quest" id="faq13">Pourquoi ma playlist est toujours vide ?</div>
Le site ne permet pas d'inscription, et retient donc la playlist grâce aux cookies. Ceux-ci doivent donc être activés sur le navigateur. (Options...)

<div class="quest" id="faq14">Pourquoi le karaoké que je recherche n'est pas sur le site ?</div>
Le site se remplit petit à petit à l'aide des utilisateurs généreux qui nous offre leurs karaokés. Un peu de patience, peut-être qu'il sera bientôt proposé !

<div class="quest" id="faq15">Puis-je proposer un karaoké existant déjà sur le site ?</div>
Oui, s'il est de meilleure qualité. L'autre ne sera pas supprimé pour autant.

<div class="quest" id="faq16">Comment est financé le site ?</div>
De la petite poche de db0 et Ryocu, deux pauvres petits étudiants pas bien riches ayant bien besoin d'un peu d'aide de la part des utilisateurs... (environ 120€/mois)

<div class="quest" id="faq17">Pourquoi y a-t-il autant de publicités sur le site ?</div>
Celles-ci permettent de rembourser en partie le prix que coûte le site. (environ 120€/mois)

